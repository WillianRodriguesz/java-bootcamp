    /**
     * Método auxiliar para atualizar a JTable com os dados do Aluno
     */
    private void atualizaLista() {
        try {
            DisciplinaDAO disciplinaDAO = new DisciplinaDAO(MDISistema.getConexao());
            List<Disciplina> lista = disciplinaDAO.listaTodos();
            DefaultTableModel dtm = (DefaultTableModel) this.tbDisciplina.getModel();
            dtm.setRowCount(0);
            for (Disciplina func : lista) {
                dtm.addRow(new Object[]{func.getCod(), func.getNome(), func.getTurma(), func.getNumAlunos(), func.getCargaHoraria()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this.mdi, String.format("Erro :%s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FormDisciplina.class.getName()).log(Level.WARNING, "Erro", ex);
        }
    }

    /**
     * Método auxiliar para limpar os campos da tela.
     */
    private void limpaTela() {
        this.txtCod.setEnabled(true);
        this.txtCod.setText(null);
        this.txtNome.setText(null);
        this.txtNumAlunos.setText(null);
        this.txtTurma.setText(null);
        this.txtCargaHoraria.setText(null);
    }

    private boolean preencheTela(Disciplina disciplina) {
        boolean ret = false;
        try {
            DisciplinaDAO disciplinaDAO = new DisciplinaDAO(MDISistema.getConexao());
            disciplina = disciplinaDAO.retrieve(disciplina);
            if (disciplina!= null && disciplina.getCod() > 0) {
                this.txtCod.setText(Integer.toString(disciplina.getCod()));
                this.txtCod.setEnabled(false);
                this.txtNome.setText(disciplina.getNome());
                this.txtTurma.setText(Integer.toString(disciplina.getTurma()));
                this.txtNumAlunos.setText(Integer.toString(disciplina.getNumAlunos()));
                this.txtCargaHoraria.setText(Integer.toString(disciplina.getCargaHoraria()));
                
                ret = true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this.mdi, String.format("Não foi possível carregar Disciplina: %s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FormDepartamento.class.getName()).log(Level.SEVERE, null, ex);
        }
        return ret;
    }

