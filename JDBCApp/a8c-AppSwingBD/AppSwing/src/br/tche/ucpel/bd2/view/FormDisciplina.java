    private int transformaString(String valor) {
        int ret = -1;
        try {
            ret = Integer.parseInt(valor);
        } catch (NumberFormatException ex) {

        }
        return ret;
    }

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            // TODO add your handling code here:
            Disciplina func = new Disciplina();

            func.setNome(this.txtNome.getText());
            func.setCod(this.transformaString(this.txtCod.getText()));
            func.setTurma(this.transformaString(this.txtTurma.getText()));
            func.setNumAlunos(this.transformaString(this.txtNumAlunos.getText()));
            func.setCargaHoraria(this.transformaString(this.txtCargaHoraria.getText()));

            DisciplinaDAO funcDao = new DisciplinaDAO(MDISistema.getConexao());
            if (func.getCod() != -1) {
                // A matrícula é um valor válido, salva no banco de dados
                funcDao.create(func);
            } else {
                System.out.println("Cod retornou -1");
            }

            JOptionPane.showMessageDialog(this.mdi, "Registro Salvo");
            this.limpaTela();
            this.atualizaLista();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this.mdi, String.format("Erro ao gravar Disciplina:%s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FormDisciplina.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btSalvarActionPerformed

