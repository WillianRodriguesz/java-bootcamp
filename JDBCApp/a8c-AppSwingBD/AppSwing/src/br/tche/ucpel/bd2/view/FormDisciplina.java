private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

    try {
        int codigo = this.transformaString(this.txtCod.getText());
        if (codigo > 0) {
            Disciplina disciplina = new Disciplina(codigo);
            DisciplinaDAO disDAO = new DisciplinaDAO(MDISistema.getConexao());
            disDAO.delete(disciplina);
            this.limpaTela();
            this.atualizaLista();
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this.mdi, String.format("Erro ao excluir Disciplina:%s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);
        Logger.getLogger(FormDisciplina.class.getName()).log(Level.WARNING, "Erro ao excluir Disciplina", ex);
    }
    this.atualizaLista();
}//GEN-LAST:event_btExcluirActionPerformed

